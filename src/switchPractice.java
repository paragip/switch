public class switchPractice {
    public static void main(String[] args) {

        int switchValue = 4;
        switch(switchValue) {
            case 1:
                System.out.println("Value is 1");
                break;

            case 2:
                System.out.println("Value is 2");
                break;

            default:
                System.out.println("Was not 1 or 2");
                break;
        }

    }
}
